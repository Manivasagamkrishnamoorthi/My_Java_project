package conditional_Statements;

public class Looping {

	public static void main(String[] args) {
//		for loop initialization;condition;increement or decreement
		for (int I = 1; I <= 5; I++) {
			System.out.println("Rajesh");
		}
		/*
		 * While loop initialization condition increement and decreement
		 */
		int B = 0;
		while (B <= 4) {
			System.out.println("Manivasagam");
			B++;
		}
		int C = 0;
		do {
			System.out.println("Ravi");
			C++;
		} while (C <=4);
	}

}
